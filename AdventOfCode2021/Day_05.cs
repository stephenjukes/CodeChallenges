            Console.WriteLine($"Part 1 overlaps: { part1Overlaps }");
            Console.WriteLine($"Part 2 overlaps: { part2Overlaps }");
        }

		private static int GetOverlaps(IEnumerable<Vent> vents, Func<Vent, bool> isRequired)
        {
			return vents
				.Where(isRequired)  
				.SelectMany(vent => vent.Range)
				.GroupBy(coOrdinate => new { coOrdinate.Column, coOrdinate.Row })
				.Where(group => group.Count() > 1)
				.Count();
		}
    }

    public class Vent
    {
        public Vent(CoOrdinate start, CoOrdinate end)
        {
            Start = start;
            End = end;
			Range = GetRange(start, end);
        }

        public CoOrdinate Start { get; set; }
        public CoOrdinate End { get; set; }
		public IEnumerable<CoOrdinate> Range { get; set; }

		private IEnumerable<CoOrdinate> GetRange(CoOrdinate a, CoOrdinate b)
        {
			var range = Math.Max(
				Math.Abs(b.Column - a.Column), 
				Math.Abs(b.Row - a.Row));

			var columnStep = b.Column.CompareTo(a.Column);
			var rowStep = b.Row.CompareTo(a.Row);

			var column = a.Column;
			var row = a.Row;

			for (var i = 0; i <= range; i++)
            {
				yield return (new CoOrdinate(column, row));

				column += columnStep;
				row += rowStep;
            }
        }
    }

    public class CoOrdinate
    {
        public CoOrdinate(int column, int row)
        {
            Column = column;
            Row = row;
        }

        public int Row { get; set; }
        public int Column { get; set; }
    }

